##method overloading :
#Method overloading is compile time polymorphism where same method name is occurred 
# in same class with differnt arguments

## Python does not support method overloading

## method overriding : 
# It is run time polymorphism where base's class method is rewritten in derived class
# with different implementation .   


##operator overloading
# It is giving extended meaning beyond thier predefined operational meaning

#For example operator + is used to add two integers as well as join two strings and 
# merge two lists. It is achievable because ‘+’ operator is overloaded by int class 
# and str class
